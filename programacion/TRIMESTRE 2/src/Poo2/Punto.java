package Poo2;

public class Punto {

	private double x;
	private double y;
	
	public Punto(double x, double y) {
		this.x=x;
		this.y=y;
	}
	
	public void setPuntoX(double x) {
		this.x=x;
	}
	public double getPuntoX() {
		return this.x;
	}
	public void setPuntoY(double y) {
		this.y=y;
	}
	public double getPuntoY() {
		return this.y;
		
	}
	
	
	
}

